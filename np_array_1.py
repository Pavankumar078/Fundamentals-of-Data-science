import numpy as np #Importing the numpy module

arr=np.array([1,2,3,4,5]) #Creating the array with list

print("Array is of type: ",type(arr)) #Printing the type of the Array

print("No. of Dimensions:",arr.ndim) #printing the array Dimensions
print("Shape of the array",arr.shape) #Shape of the Array
print("Size of the array: ",arr.size) #Printing the total no.of elements in the array
print("Array stores elements of the type: ",arr.dtype) #Printing the data type of elements
print("Array elements: ",arr) #printing the array elements.
